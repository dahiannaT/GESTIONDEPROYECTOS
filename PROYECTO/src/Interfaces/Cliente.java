    public void eliminarCliente(){
    int filaSelec =TablaCliente.getSelectedRow();
    
        try {
            String query=" delete from cliente where id_cliente="+TablaCliente.getValueAt(filaSelec,0);
            
            Statement st=con.createStatement();
            
            int n=st.executeUpdate(query);
            
            if (n>=0){
             JOptionPane.showMessageDialog(null, "Registro Cliente Eliminado");   
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Registro Cliente No Eliminado" + e.getMessage()); 
        }
    
    }
    
    public void insertarCliente() {

        try {
         
          String query = "insert into cliente (id_cliente, nombreCliente, numTel, correo, empresa, direccion) values (?,?,?,?,?,?)";

            PreparedStatement pst = con.prepareStatement(query);
            pst.setInt(1, Integer.parseInt(txtId.getText()));
            pst.setString(2, txtNombre.getText());
            pst.setInt(3, Integer.parseInt(txtTel.getText()));
            pst.setString(4, txtCorreo.getText());
            pst.setString(5, txtEmpresa.getText());
            pst.setString(6, txtDireccion.getText());

            pst.execute();
            JOptionPane.showMessageDialog(null, "Registro CLiente Guardado");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Registro Cliente No Guardado" + e.getMessage());
        }
    }

    
