    public void listarCliente(){
        String[] titulos ={"id_cliente", "nombreCliente", "numTel", "correo", "empresa", "direccion"};
        String[] registros= new String[7];
        
        DefaultTableModel modelo = new DefaultTableModel(null,titulos);
        
        String query = "select * from cliente";
        try {
            Statement st =con.createStatement();
            ResultSet rs= st.executeQuery(query);
            
            while(rs.next()){
                registros[0]=rs.getString("id_cliente");
                registros[1]=rs.getString("nombreCliente");
                registros[2]=rs.getString("numTel");
                registros[3]=rs.getString("correo");
                registros[4]=rs.getString("empresa");
                registros[5]=rs.getString("direccion");
                
                modelo.addRow(registros);                
            }
            
            TablaCliente.setModel(modelo);
        } catch (Exception e) {
           JOptionPane.showMessageDialog(null, "Error al listar CLientes" + e.getMessage());       
        }   
    }
    
    
     
     
