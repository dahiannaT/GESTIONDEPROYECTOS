    public void insertarMetodologia() {

        try {
         
          String query = "insert into Metodologia (id_metodologia, nombreMetodologia, numfase) values (?,?,?)";

            PreparedStatement pst = con.prepareStatement(query);
            pst.setInt(1, Integer.parseInt(txtIdM.getText()));
            
            int seleccionado=cbMetodologias.getSelectedIndex();
            pst.setString(2,cbMetodologias.getItemAt(seleccionado));
            pst.setInt(3, Integer.parseInt(txtNunF.getText()));

            pst.execute();
            JOptionPane.showMessageDialog(null, "Registro Metodologias Guardado");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Registro Metodologias No Guardado" + e.getMessage());
        }
    }
    
