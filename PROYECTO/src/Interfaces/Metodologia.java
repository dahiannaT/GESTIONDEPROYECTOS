     public void listarMetodologias(){
        String[] titulos ={"id_metodologia", "nombreMetodologia","numfase"};
        String[] registros= new String[3];
        
        DefaultTableModel modelo = new DefaultTableModel(null,titulos);
        
        String query = "select * from Metodologia";
        try {
            Statement st =con.createStatement();
            ResultSet rs= st.executeQuery(query);
            
            while(rs.next()){
                registros[0]=rs.getString("id_metodologia");
                registros[1]=rs.getString("nombreMetodologia");
                registros[2]=rs.getString("numfase");
                
                modelo.addRow(registros);                
            }
            
            TablaMetodologias.setModel(modelo);
        } catch (Exception e) {
           JOptionPane.showMessageDialog(null, "Error al listar Metodologias" + e.getMessage());       
        }   
    }
     
     public void eliminarMetodologias(){
    int filaSelec =TablaMetodologias.getSelectedRow();
    
        try {
            String query=" delete from Metodologia where id_metodologia="+TablaMetodologias.getValueAt(filaSelec,0);
            
            Statement st=con.createStatement();
            
            int n=st.executeUpdate(query);
            
            if (n>=0){
             JOptionPane.showMessageDialog(null, "Registro Metodologias Eliminado");   
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Registro Metodologias No Eliminado" + e.getMessage()); 
        }
    
    }
     
     
     public void filtrarMetodologias(String valor){
        String[] titulos ={"id_metodologia", "nombreMetodologia","numfase"};
        String[] registros= new String[3];
        
        DefaultTableModel modelo = new DefaultTableModel(null,titulos);
        
        String query = "select * from Metodologia where nombreMetodologia like '%"+ valor + "%' ";
        try {
            Statement st =con.createStatement();
            ResultSet rs= st.executeQuery(query);
            
            while(rs.next()){
                registros[0]=rs.getString("id_metodologia");
                registros[1]=rs.getString("nombreMetodologia");
                registros[2]=rs.getString("numfase");
                
                modelo.addRow(registros);                
            }
            
            TablaMetodologias.setModel(modelo);
        } catch (Exception e) {
           JOptionPane.showMessageDialog(null, "Error al filtrar Metodologias" + e.getMessage());       
        }   
     }    
     
     public void actualizarMetodologia() {

        try {
         
          String query = "update Metodologia Set nombreMetodologia=?, numfase=? where id_metodologia =?";

            int filaSelect=TablaMetodologias.getSelectedRow();
          
            PreparedStatement pst = con.prepareStatement(query);
            
            int seleccionado=cbMetodologias.getSelectedIndex();
            pst.setString(1,cbMetodologias.getItemAt(seleccionado));
            pst.setInt(2, Integer.parseInt(txtNunF.getText()));
            pst.setInt(3, Integer.parseInt(txtIdM.getText()));
                    
            pst.executeUpdate();
            pst.close();
            JOptionPane.showMessageDialog(null, "Registro Metodologia Actualizado");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Registro Metodologia No Actualizado" + e.getMessage());
        }
    }
     
